#include "libft.h"

int	ft_isalnum(int c)
{
	char	ch;

	ch = (char) c;
	if (c > 64 && c < 91)
		return (1);
	if (c > 96 && c < 123)
		return (1);
	if (c >= 48 && c <= 57)
		return (1);
	return (0);
}
